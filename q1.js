alert("This Program Checks if the Entered Number Is greater Than 10")
let number= parseFloat(prompt("Enter the number to check if it is greater than 10"))

if(number>10){
    console.log(number  + " is Greater Than 10");
    alert(number  + " is Greater Than 10")
}
else{
    console.log(number + " is not Greater than 10");
    alert(number + " is not Greater than 10")
}